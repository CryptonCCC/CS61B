/** An Integer tester created by Flik Enterprises. */
public class Flik {
    /** Integer is a class, no different from any other in the Java language.
     * Variables of type Integer store references to Integer objects,
     * just as with any other reference (object) type.
     * The two references are not identical when a, b > 127
     */
    public static boolean isSameNumber(Integer a, Integer b) {
        return a == b;
    }
}
